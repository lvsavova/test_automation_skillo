package lecture04.examples.operators;

public class AutoOperatorDemo {
    public static void main(String args[]) {
        int num1 = 100;
        int num2 = 200;
        num1++;
        num2--;
        System.out.println("num1++ is: " + num1);
        System.out.println("num2-- is: " + num2);
    }
}
